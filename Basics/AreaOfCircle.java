//Area of a circle

import java.util.Scanner;

public class AreaOfCircle {
    public static void main(String[]Arg){
        double area,r;
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter radius: ");
        r=sc.nextDouble();
        area= 3.1415*r*r;
        System.out.println("Area is : "+area);;
    }
    
}
